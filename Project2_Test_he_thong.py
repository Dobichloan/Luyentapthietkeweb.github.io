    # NODE 7: STATUS DISPLAY
    # -----------------------------------------
    def show_parking_status(self):
        """
        Hiển thị tình trạng theo từng zone (used/total).
        """
        print("\n--- Tình trạng bãi đỗ ---")

        def print_group(prefix, title):
            print(f"\n{title}:")
            for i in range(1, 11):
                z = f"{prefix}{i}"
                used = self.zone_count[z]
                cap = self.max_capacity_zone[z]
                print(f"  Zone {z}: {used} / {cap} đã sử dụng (còn {cap - used}).")

        print_group("C", "Tầng 1 (Ô tô)")
        print_group("M", "Tầng 2 (Xe máy)")
        print_group("B", "Tầng 3 (Xe linh tinh)")

        print(f"\nTổng xe đang IN trong bãi: {self.current_total}")

    # ============================================================
    # INTERNAL HELPERS
    # ============================================================

    def _prefix_for_type(self, vehicle_type: str):
        mapping = {"car": "C", "motorbike": "M", "other": "B"}
        return mapping.get(vehicle_type.strip().lower())

    def _has_available_zone(self, prefix: str) -> bool:
        for i in range(1, 11):
            z = f"{prefix}{i}"
            if self.zone_count[z] < self.max_capacity_zone[z]:
                return True
        return False

    def _assign_parking_slot(self, vehicle_type: str, plate: str, zone: str):
        # capacity check
        if self.zone_count[zone] >= self.max_capacity_zone[zone]:
            print(f"Zone {zone} đã đầy. Xe sẽ vào WAITING.")
            self._enqueue_waiting(vehicle_type, plate)
            return

        if not self.free_slots_pool[zone]:
            print(f"Hết slot trong {zone}. Xe sẽ vào WAITING.")
            self._enqueue_waiting(vehicle_type, plate)
            return

        slot = self.free_slots_pool[zone].pop()
        self.zone_occupied[zone].add(slot)

        now = time.time()
        self.index[plate] = {
            "vehicle_type": vehicle_type,
            "zone": zone,
            "slot": slot,
            "time_in": now,
            "time_out": None,
            "status": "IN",
            "spot": f"{zone} - Ô {slot + 1}",
        }

        self.zone_count[zone] += 1
        self.current_total += 1

        print(f"✅ Xe {plate} vào {zone} - Ô {slot + 1}.")

        if not self._check_invariants():
            print("⚠️ Cảnh báo: invariant bị vi phạm sau CHECK-IN.")

    def _admit_waiting_fifo(self):
        """
        Admit WAITING vehicles in FIFO order when slots become available.
        Auto-assign to best recommended zone (top 1).
        """
        while self.waiting_queue:
            v_type, plate = self.waiting_queue[0]
            prefix = self._prefix_for_type(v_type)
            if prefix is None:
                self.waiting_queue.popleft()
                if plate in self.index:
                    self.index[plate]["status"] = "OUT"
                continue

            rec = self.suggest_zone(v_type, top_k=1)
            if not rec:
                break

            self.waiting_queue.popleft()
            self._assign_parking_slot(v_type, plate, rec[0])
            print(f"➡️ Xe WAITING {plate} đã được nhận vào bãi (auto-assign {rec[0]}).")

    def _check_invariants(self) -> bool:
        for z in self.zones:
            if self.zone_count[z] != len(self.zone_occupied[z]):
                return False

        for z in self.zones:
            if len(self.free_slots_pool[z]) + len(self.zone_occupied[z]) != self.max_capacity_zone[z]:
                return False

        if self.current_total != sum(self.zone_count[z] for z in self.zones):
            return False

        for _, rec in self.index.items():
            if rec["status"] == "IN":
                if rec["zone"] is None or rec["slot"] is None:
                    return False

        return True

